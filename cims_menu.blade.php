					var icon = this.querySelector('i');
					if (icon) icon.style.color = '#fff';
					// Show the sd_tooltip_teal briefly
					var tooltip = this.parentElement.querySelector('.sd-menu-tooltip');
					if (tooltip) {
						tooltip.classList.add('show');
						// Auto-hide after 2 seconds
						if (tooltipTimer) clearTimeout(tooltipTimer);
						tooltipTimer = setTimeout(function() {
							tooltip.classList.remove('show');
						}, 2000);
					}

					// Check if third-level menu needs to flip to left
					var thirdLevel = this.parentElement.querySelector('.mm-collapse');
					if (thirdLevel) {
						var rect = this.getBoundingClientRect();
						var menuWidth = 200; // Approximate width of third-level menu
						var screenWidth = window.innerWidth;

						// If menu would go off-screen on the right, flip to left
						if (rect.right + menuWidth > screenWidth) {
							thirdLevel.classList.add('flip-left');
						} else {
							thirdLevel.classList.remove('flip-left');
						}
					}
				});
				link.addEventListener('mouseleave', function() {
					this.style.background = '';
					this.style.color = '';
					this.style.borderBottom = '';
					var icon = this.querySelector('i');
					if (icon) icon.style.color = '';
					// Hide the sd_tooltip_teal immediately on leave
					var tooltip = this.parentElement.querySelector('.sd-menu-tooltip');
					if (tooltip) tooltip.classList.remove('show');
					if (tooltipTimer) clearTimeout(tooltipTimer);
				});
			});
		});
